    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if (tfId.getText().isBlank() || tfNama.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "Data input ada yg kosong!");
        } else {
            boolean created = pelangganService.insert(new Pelanggan(tfId.getText(), tfNama.getText(), String.valueOf(comboTahun.getSelectedItem()), String.valueOf(comboJenis.getSelectedItem())));
            if (created) {
                JOptionPane.showMessageDialog(this, "Berhasil simpan data pelanggan");
                updateTable();
                reset();
            } else {
                JOptionPane.showMessageDialog(this, "Gagal simpan data pelanggan");
            }
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

