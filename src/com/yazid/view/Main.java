    public void initTahunCombo() {
        ArrayList<String> dataTahun = new ArrayList<>();
        tahunModel = new DefaultComboBoxModel<>();

        for (int i = 1990; i <= 2000; i++) {
            tahunModel.addElement(String.valueOf(i));
        }

        comboTahun.setModel(tahunModel);
        comboTahun.setSelectedIndex(0);
    }

    public void initTable() {
        String[] header = {
            "Nomor", "Nama", "Tahun Lahir", "Jenis"
        };

        tablePelangganModel = new DefaultTableModel(header, 0);
        tablePelanggan.setModel(tablePelangganModel);
        tampilAll();
    }

    public void tampilAll() {
        ArrayList<Pelanggan> dataPelanggan = pelangganService.all();
        for (Pelanggan data : dataPelanggan) {
            addDataToTablePelanggan(data);
        }
    }

    public boolean tampilWithData(ArrayList<Pelanggan> dataPelanggan) {
        for (Pelanggan data : dataPelanggan) {
            addDataToTablePelanggan(data);
        }
        return dataPelanggan.size() > 0;
    }

    public void updateTable() {
        tablePelangganModel.setNumRows(0);
        tampilAll();
    }

    public void addDataToTablePelanggan(Pelanggan pelanggan) {
        String[] data = {
            pelanggan.getId(), pelanggan.getNama(), pelanggan.getTahunLahir(), pelanggan.getJenis()
        };
        tablePelangganModel.addRow(data);
    }

