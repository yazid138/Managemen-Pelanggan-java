    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        // TODO add your handling code here:
        tablePelangganModel.setNumRows(0);
        if (tfCari.getText().isBlank()) {
            tampilAll();
        } else {
            ArrayList<Pelanggan> dataPelanggan = pelangganService.where("nama LIKE '%" + tfCari.getText() + "%'");
            boolean exist = tampilWithData(dataPelanggan);
            if (!exist) {
                JOptionPane.showMessageDialog(this, "Data pelanggan tidak ada");
            }
        }
    }//GEN-LAST:event_btnCariActionPerformed

