    private void tablePelangganMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablePelangganMouseClicked
        // TODO add your handling code here:
        int i = tablePelanggan.getSelectedRow();

        if (i >= 0) {
            tfId.setText(String.valueOf(tablePelangganModel.getValueAt(i, 0)));
            tfNama.setText(String.valueOf(tablePelangganModel.getValueAt(i, 1)));
            comboTahun.setSelectedItem(tablePelangganModel.getValueAt(i, 2));
            comboJenis.setSelectedItem(tablePelangganModel.getValueAt(i, 3));
        }
    }//GEN-LAST:event_tablePelangganMouseClicked

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        int confirm = JOptionPane.showConfirmDialog(this, "Apakah anda yakin ingin mengahapus data tersebut");
        if (confirm == 0) {
            boolean deleted = pelangganService.delete(tfId.getText());

            if (deleted) {
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus");
                updateTable();
                reset();
            } else {
                JOptionPane.showMessageDialog(this, "Data gagal dihapus");
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

